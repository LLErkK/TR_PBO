    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int rowIndex = jTable1.getSelectedRow();
        if(rowIndex!= -1){
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int menuId = (int) model.getValueAt(rowIndex,0);
            //controller
            AdminMenuController amc = new AdminMenuController();
            Menu modelMenu = amc.getMenuById(menuId);
            if(modelMenu!=null){
                txtNama.setText(modelMenu.getNama_menu());
                txtHarga.setText(String.valueOf(modelMenu.getHarga()));
                if(modelMenu.getKategori().equals("makanan")){
                    jComboBox1.setSelectedItem("makanan");
                }else jComboBox1.setSelectedItem("minuman");
                lblId.setText(String.valueOf(modelMenu.getId()));
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(lblId.getText().equals("id")){
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Apakah Anda yakin ingin menghapus user ini?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            AdminMenuController amc = new AdminMenuController();
            int id = Integer.parseInt(lblId.getText());
            boolean status = amc.deleteMenu(id);

            if (status) {
                JOptionPane.showMessageDialog(this, "User berhasil dihapus!");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus user. Silakan coba lagi.");
            }

            this.dispose();
            new AdminBarang().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Penghapusan dibatalkan.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

