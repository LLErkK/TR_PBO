    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {
        if(lblId.getText().equals("id") && txtNama.getText().isEmpty() && txtHarga.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Tidak Boleh Kosong!");
            return;
        }
        if(lblId.getText().equals("id")){
            String nama = txtNama.getText();
            double harga = Double.parseDouble(txtHarga.getText());
            String tipe = (String) jComboBox1.getSelectedItem();
            AdminMenuController amc = new AdminMenuController();
            boolean status = amc.createMenu(nama,tipe,harga);
            if(status){
                JOptionPane.showMessageDialog(this,"Berhasil Membuat Menu baru");
                this.dispose();
                new AdminBarang().setVisible(true);
                return;
            }
        }
        int id = Integer.parseInt(lblId.getText());
        String nama = txtNama.getText();
        double harga = Double.parseDouble(txtHarga.getText());
        String tipe = (String) jComboBox1.getSelectedItem();
        AdminMenuController amc = new AdminMenuController();
        boolean status = amc.updateMenuById(id,nama,tipe,harga);
        if(status){
            JOptionPane.showMessageDialog(this,"Berhasil MengUpdate Menu!");
            this.dispose();
            new AdminBarang().setVisible(true);
        }
    }                                         

